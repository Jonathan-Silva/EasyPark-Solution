    private void Limpar(){
        txtCod.setText("");
        txtPlaca.setText("");
        cbVaga.setSelectedIndex(0);
    }
    
    private void Bloqueia (boolean Bloqueiar)
    {
        if (Bloqueiar == true) 
        {
            txtPlaca.setEnabled(false);
            cbVaga.setEnabled(false);
           
        }
        else
        {
            txtPlaca.setEnabled(true);
            cbVaga.setEnabled(true);
           
        }
          
    }

    
    private void Atribuir(boolean Vai)
    {
        if (Vai==true) {
            Veiculo.setId(txtCod.getText());
            Veiculo.setPlaca(txtPlaca.getText());
            Veiculo.setIdTipoVaga((cbVaga.getSelectedItem().equals("CARRO"))? "1":"2");
            Veiculo.setIdMotorista(Ctrl_Util.IdCliente);           
        }
        else
        {
            txtCod.setText(Veiculo.getId());
            txtPlaca.setText(Veiculo.getPlaca());
            cbVaga.setSelectedIndex(Integer.parseInt(Veiculo.getIdTipoVaga()));
        }

    }
    
    private  boolean Verifica()
    {
        String x[] = new String[4];
        x[0] = txtCod.getText();
        x [1] = txtPlaca.getText();
        x [2] = String.valueOf(cbVaga.getSelectedIndex());
        x [3] = Ctrl_Util.IdCliente;
        
        
        for (int i = 0; i < 4; i++) {
            if(x[i].equals(""))
            {
                Ctrl_Msg.Informa(Ctrl_Msg.MsgICampos);
                return false; 
            }
        }
        return true;
    }
    
    

