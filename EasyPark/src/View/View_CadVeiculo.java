    private void PopularTabela() {
        List<Ctrl_Veiculo> ListaDeVeiculos = Model.Model_Veiculo.Busca(Ctrl_Util.IdCliente);
        
        DefaultTableModel model = (DefaultTableModel) tbVeiculos.getModel();
        for (int i = 0; i < ListaDeVeiculos.size(); i++) {
            String Tipo = (ListaDeVeiculos.get(i).getIdTipoVaga().equals("1"))? "Carro" : "Moto";
            Object[] linha = {ListaDeVeiculos.get(i).getId(),ListaDeVeiculos.get(i).getPlaca(), Tipo};
            model.addRow(linha);
        }
        tbVeiculos.setModel(model);
    }
    
    private void LimparTabela(){
        tbVeiculos.setModel(new javax.swing.table.DefaultTableModel(
        new Object [][] {},
        new String [] {"Código", "Placa", "Tipo"}));
    }
